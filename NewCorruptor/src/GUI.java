    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser c = new JFileChooser(".");
        c.setMultiSelectionEnabled(false);
        c.showOpenDialog(this);
        File x = c.getSelectedFile();
        if (x == null) {
            return;
        }
        in.setText(x.getPath());
        if (out.getText().isEmpty()) {
            int dot = in.getText().lastIndexOf(".");
            if (dot == -1) {
                out.setText(in.getText() + "_Corrupted");
            } else {
                out.setText(in.getText().substring(0, dot) + "_Corrupted" + in.getText().substring(dot, in.getText().length()));
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser c = new JFileChooser(".");
        c.setMultiSelectionEnabled(false);
        c.showSaveDialog(this);
        File x = c.getSelectedFile();
        if (x == null) {
            return;
        }
        out.setText(x.getPath());
    }//GEN-LAST:event_jButton3ActionPerformed
    

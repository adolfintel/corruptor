    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (c != null) {
            stopped = true;
            c.stopWorking();
            try {
                c.join();
            } catch (InterruptedException ex) {
            }
            c = null;
        } else {
            new Thread() {
                public void run() {
                    long startT = System.currentTimeMillis();
                    boolean ok = false;
                    int closeOp = getDefaultCloseOperation();
                    String buttonText = jButton1.getText();
                    String err = null;
                    try {
                        for (Component x : jPanel1.getComponents()) {
                            x.setEnabled(false);
                        }
                        jButton1.setEnabled(true);
                        jButton1.setText(ResourceBundle.getBundle("GUI").getString("stop"));
                        setDefaultCloseOperation(DO_NOTHING_ON_CLOSE);
                        if (in.getText().isEmpty()) {
                            throw new Exception(ResourceBundle.getBundle("GUI").getString("noInput"));
                        }
                        if (out.getText().isEmpty()) {
                            throw new Exception(ResourceBundle.getBundle("GUI").getString("noOutput"));
                        }
                        if (!p.getInputVerifier().verify(p)) {
                            throw new Exception(ResourceBundle.getBundle("GUI").getString("invalidP"));
                        }
                        if (!skip.getInputVerifier().verify(skip)) {
                            throw new Exception(ResourceBundle.getBundle("GUI").getString("invalidSkip"));
                        }
                        if (!skip.getInputVerifier().verify(skip1)) {
                            throw new Exception(ResourceBundle.getBundle("GUI").getString("invalidSkip1"));
                        }
                        c = new Corruptor(in.getText(), out.getText(), Double.parseDouble(p.getText()), bitwise.isSelected(), Integer.parseInt(skip.getText()),Integer.parseInt(skip1.getText()));
                        c.start();
                        while (c != null && c.isAlive() && c.getProgress() < 1) {
                            prog.setValue((int) (100 * c.getProgress()));
                        }
                        prog.setValue(100);
                        ok = true;
                    } catch (Throwable t) {
                        ok = false;
                        err = t.getMessage();
                    }
                    if (c != null) {
                        c.stopWorking();
                    }
                    c = null;
                    jButton1.setText(buttonText);
                    if (!stopped) {
                        if (ok) {
                            JOptionPane.showMessageDialog(rootPane, ResourceBundle.getBundle("GUI").getString("success") + (System.currentTimeMillis() - startT) + " ms", getTitle(), JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(rootPane, ResourceBundle.getBundle("GUI").getString("error") + err, getTitle(), JOptionPane.ERROR_MESSAGE);
                        }
                    } else {
                        stopped = false;
                    }
                    prog.setValue(0);
                    for (Component x : jPanel1.getComponents()) {
                        x.setEnabled(true);
                    }
                    setDefaultCloseOperation(closeOp);
                }
            }.start();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
